namespace CALCULATE

{
    int sum(int a, int b)
    {
        return a + b;
    }
    float sum(float a, float b)
    {
        return a + b;
    }
    double sum(double a, double b)
    {
        return a + b;
    }
    int mul(int a, int b)
    {
        return a * b;
    }
    float mul(float a, float b)
    {
        return a * b;
    }
    double mul(double a, double b)
    {
        return a * b;
    }
}